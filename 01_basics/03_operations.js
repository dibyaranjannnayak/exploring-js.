//arithmetic operation
console.log(2 ** 3)       //exponentiation 2^3
console.log(5 % 4)        //modulus  reminder

//comparision operations
console.log(5 == '5')       //loosely equality check
console.log(5 === '5')      //strictly equlity check

//logical operations
console.log(true && false)  //and
console.log(true || false)  //or
console.log(!false)         //not

//bitwise operations
console.log(5 & 3)      //bitwiseAnd
console.log(5 | 3)      //bitwiseOr
console.log(5 ^ 3)      //bitwiseXor
console.log(~5)         //bitwiseNot
console.log(5 << 1)     //leftShift
console.log(5 >> 1)     //rightShift

console.log(2 + '3')    //23
console.log('3' + 2 + 5)    //325
console.log(2 + 1 + '7')    //37
console.log(2 + 3 * 5 + 2 / 2)  //18

